CREATE TABLE Lineitem (
	L_ORDERKEY		BIGINT NOT NULL,
	L_PARTKEY		BIGINT NOT NULL,
	L_SUPPKEY		BIGINT NOT NULL,
	L_RETURNFLAG	CHAR(1),
	L_LINESTATUS	CHAR(1),
    L_RETURNFLAG_INT	INT,
    L_LINESTATUS_INT	INT,
	L_QUANTITY		DECIMAL,
	L_EXTENDEDPRICE	DECIMAL,
	L_DISCOUNT		DECIMAL,
	L_TAX			DECIMAL,
	L_SHIPDATE		DATE,
	PRIMARY KEY (L_ORDERKEY)
);

